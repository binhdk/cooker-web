<h2>Cách nấu</h2>
<h4>– Nấm rơm rửa sạch, cắt nhỏ.<br/>
– Trứng gà bỏ lòng đỏ chỉ lấy lòng trắng.<br/>
– Thịt heo rửa sạch để cho ráo nước. Bạn có thể chọn mua loại thịt tùy theo sở thích ăn thịt nhiều hay mở nhiều của bạn (tuy nhiên nếu thịt quá nhiều thì nhân khổ qua sẽ bị cứng và không béo). Khi thịt đã ráo nước ta tiến hành bằm hoặc xay nhuyễn thịt. Xay xong, thì cho muối, đường, hạt nêm, tiêu giả nhuyễn, tỏi phi vàng, hành lá xắt nhuyễn, nấm rơm, lòng trắng trứng vào trộn đều. Nếu có thời gian bạn dùng chài giả thịt khoảng 5-10 phút thì thịt sẽ giai ngon hơn. Ướp xong bạn có thể lấy một ít thịt đem nướng ăn thử xem như vậy đã vừa miệng chưa và điều chỉnh gia vị.<br/>
– Khổ qua bỏ phần ruột và rửa sạch, để úp phần mặt cắt xuống cho ráo nước. Nếu chọn trái vừa thì bổ trái khổ qua theo chiều dọc, còn trái lớn, dài thì có thể cắt làm đôi theo chiều ngang hoặc cũng có thể cắt thành từng khoanh. Đối với những khúc khổ qua bạn cắt ngang bạn dùng một chiếc đũa xăm thủng phần cho thoát khí để khi dồn thịt vào thịt sẽ giẻ hơn.<br/>
– Xương rửa xong đem đi hầm để lấy nước ngọt, trong quá trình hầm vớt bỏ bọt nổi lên để nước canh khổ qua hầm xương được trong hơn. Nếu bạn không có nhiều thời gian bạn có thể bỏ qua khâu này.<br/>
– Tiếp theo là dồn đầy thịt vào khổ qua. Những trái khổ qua bạn bổ dọc có thể dùng hành lá trộn sơ nước sôi để buộc ngang cho đẹp.<br/>
– Dồn xong đợi khi nước sôi thì thả khổ qua vào hầm. Để có cách nấu khổ qua nhồi thịt xanh,trái khổ qua không bị vàng ta phải vớt hết bọt và không đậy nắp, với cách này nước hầm cũng trong hơn. Đun lửa vừa để khổ qua mềm từ từ, nêm gia vị vừa ăn nhé, cho thêm hành lá cắt khúc vào trước khi tắt lửa và hoàn thành cách nấu khổ qua hầm thịt. Cuối cùng khi múc ra tô cho một ít rau mùi lên trên cho thơm.<br/>
**Cách nấu canh khổ qua nhồi thịt tuy đơn giản và quen thuộc nhưng bạn cũng nên lưu ý một số điểm sau:<br/>
– Thịt phải để ráo nước nếu không sẽ làm giảm độ giai của nhân thịt.<br/>
– Nếu không thích ăn quá đắng bạn nên chọn những trái khổ qua có gai nở to sẽ ít đắng hơn.<br/>
– Nấm rơm bạn cũng có thể để nguyên nấm cho vào nước hầm cũng rất ngọt nước và ngon nhé.<br/>
– Nếu thịt bị dư thì vò viên và nấu chung cũng rất ngon.<br/>
– Một cách nấu khổ qua hầm khác cũng ngon không kém là có thể dùng cá xay để thay thịt hoặc trộn cả cá và thịt chung.<br/>
– Lưu ý cuối cùng là nếu bạn quen ăn nước mắm thì chỉ nên chấm khổ qua hầm thôi chứ đừng nêm vào nồi khi nấu sẽ làm cho nước của món khổ qua nhồi thịt hầm bị chua đấy.<br/>
